// 11.Find perimeter of rectangle formula : P=2(l+w)
#include<stdio.h>
int main()
{
    int l,w;
    int perimeter;
    printf("Enter the Length and The Width of the Rectangle ");
    scanf("%d" "%d",&l,&w);
    perimeter=2*(l+w);
    printf("The Perimeter of the rectangle is %d",perimeter);

    return 0;
}